    private void jtLoginFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtLoginFocusLost
        if(jtLogin.getText().isEmpty()){
            jtLogin.setBackground(Color.decode("#FF0000"));
        }else{
            jtLogin.setBackground(Color.white);
            jtLogin.setForeground(Color.black);
            jtLogin.setBackground(Color.decode("#f98000"));
        }
    }//GEN-LAST:event_jtLoginFocusLost


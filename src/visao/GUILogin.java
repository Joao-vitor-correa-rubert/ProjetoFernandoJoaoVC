    private void logar(){
        try {
            
            usuarioServicos us = servicosFactory.getUsuarioServicos();
            UsuarioVO uVO = new UsuarioVO();
            uVO.setLogin(jtLogin.getText());
            uVO.setSenha(String.valueOf(jpSenha.getPassword()));
            
            if(us.validarSenha(uVO)){
                //Chamando o menu
                abrirMenuPrincipal();
                //Setando visibilidade da GUILogin
                this.setVisible(false);
            }else{
                cont++;
                
                jlAviso.setText(cont+"ª tentativa! Login/senha inválido(s)!");
                
                if(cont==3){
                    System.exit(EXIT_ON_CLOSE);
                }
            }//fecha else
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Erro! "+e.getMessage());
        }//fecha catch
    }
    private void abrirMenuPrincipal(){
        MenuPrincipal mp = new MenuPrincipal();
        mp.setVisible(true);
    }//fecha abrirGUICadProduto
    
    
